SET room = t.cabinet
FROM temp_cabinets t
WHERE TRIM(e.name) = TRIM(t.full_name)
   OR e.name ILIKE '%' || TRIM(t.full_name) || '%'
   OR TRIM(t.full_name) ILIKE '%' || TRIM(e.name) || '%';

