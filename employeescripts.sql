    id SERIAL PRIMARY KEY,
    name VARCHAR(255),
    position VARCHAR(255),
    internal_phone VARCHAR(50),
    phone_city VARCHAR(50),
    phone_mobile VARCHAR(50),
    email VARCHAR(255),
    room VARCHAR(50),
    department_id INTEGER REFERENCES departments(id) ON DELETE SET NULL
