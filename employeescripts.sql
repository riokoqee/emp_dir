SELECT COUNT(*) AS "С внутренним номером" FROM employees WHERE internal_phone IS NOT NULL;

SELECT d.name AS department, COUNT(*) AS employees
FROM employees e
JOIN departments d ON e.department_id = d.id
GROUP BY d.name
ORDER BY d.name;